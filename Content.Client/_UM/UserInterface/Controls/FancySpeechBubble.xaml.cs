using System.Diagnostics.CodeAnalysis;
using System.Numerics;
using System.Text.RegularExpressions;
using Content.Client.UserInterface.Systems.Chat;
using Content.Shared.Chat;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.RichText;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client._UM.UserInterface.Controls;

[GenerateTypedNameReferences]
public sealed partial class FancySpeechBubble : Control
{
    public static readonly Regex FontRegex = new(@"\[font=([^\s\]]+)\s+size=(\d+)\]", RegexOptions.IgnoreCase | RegexOptions.Compiled);

    private readonly ChatUIController _chatUIController;

    private readonly int _wordWrapLength = 32;
    private int _fontSize;
    private string _font;
    private readonly Color? _color;
    private readonly bool _forceFont;
    private readonly int? _thicknessOverride;
    private readonly string? _contentTag;
    private readonly string? _glyph;

    public FancySpeechBubble(ChatMessage message,
        int fontSize = 12,
        string font = "Minecraft",
        bool forceFont = false,
        Color? fontColor = null,
        int? thicknessOverride = null,
        string? contentTag = null,
        string? glyph = null)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _chatUIController = UserInterfaceManager.GetUIController<ChatUIController>();
        _color = fontColor;
        _fontSize = fontSize;
        _font = font;
        _forceFont = forceFont;
        _thicknessOverride = thicknessOverride;
        _contentTag = contentTag;
        _glyph = glyph;

        BuildLines(message, _contentTag);
    }

    /// <summary>
    /// Gets the font that the message has.
    /// </summary>
    private (string? FontName, int? FontSize) ParseFont(string input)
    {
        var match = FontRegex.Match(input);

        if (match.Success)
        {
            string fontName = match.Groups[1].Value;
            int fontSize = int.Parse(match.Groups[2].Value);
            return (fontName, fontSize);
        }

        return (null, null);
    }

    public static bool TryGetStringInsideTag(ChatMessage message,
        string tag,
        [NotNullWhen(true)] out string? outMessage)
    {
        outMessage = null;

        if (message.BubbleMessage == null)
            return false;

        var rawmsg = message.BubbleMessage;
        var tagStart = rawmsg.IndexOf($"[{tag}]");
        var tagEnd = rawmsg.IndexOf($"[/{tag}]");
        if (tagStart < 0 || tagEnd < 0)
        {
            outMessage = "";
            return true; //?? idk just in case
        }

        tagStart += tag.Length + 2;

        outMessage = rawmsg.Substring(tagStart, tagEnd - tagStart);
        return true;
    }

    private void BuildLines(ChatMessage chatMessage, string? tag)
    {
        if (chatMessage.BubbleMessage == null)
            return;

        string? messageString;
        if (tag == null || !TryGetStringInsideTag(chatMessage, tag, out messageString))
            messageString = chatMessage.BubbleMessage;

        //strip markup cheap hack
        var tmpMsg = new FormattedMessage();
        tmpMsg.AddMarkupOrThrow(messageString);

        var glyphed = false;

        var font = ParseFont(chatMessage.BubbleMessage);

        if (font.FontName != null && !_forceFont)
            _font = font.FontName;

        if (font.FontSize != null && !_forceFont)
            _fontSize = font.FontSize.Value;

        var wraptest = WordWrapHelper.WordWrap(tmpMsg.ToString(), _wordWrapLength);

        foreach (var rawMessage in wraptest)
        {
            var message = _chatUIController.HighlightString(rawMessage); //highlight the text here.. i know

            var msg = new FormattedMessage();

            msg.PushTag(new MarkupNode("font",
                new MarkupParameter(_font),
                new Dictionary<string, MarkupParameter>()
                {
                    { "size", new MarkupParameter(_fontSize) }
                }));
            if (_color != null)
                msg.PushColor(_color.Value);

            msg.AddMarkupOrThrow(message);
            msg.Pop();
            msg.Pop();

            var thickness = _fontSize / 6;

            if (_thicknessOverride != null)
                thickness = _thicknessOverride.Value;

            var label = new OutlineRichTextLabel
            {
                Thickness = thickness,
                StyleClasses = { "bubbleContent" },
                HorizontalAlignment = HAlignment.Center,
                Margin = new Thickness(thickness * 3, thickness),
            };
            label.SetMessage(msg, tagsAllowed: [typeof(FontTag), typeof(ColorTag), typeof(BoldTag)]);

            var labelContainer = new BoxContainer
            {
                Align = BoxContainer.AlignMode.Center,
                VerticalAlignment = VAlignment.Center,
            };

            if (_glyph != null && !glyphed)
            {
                var textureRect = new TextureRect
                {
                    TexturePath = _glyph,
                    TextureScale = new Vector2(2, 2),
                };

                labelContainer.AddChild(textureRect);
                glyphed = true;
            }


            labelContainer.AddChild(label);
            TextContainer.AddChild(labelContainer);

        }
    }
}
